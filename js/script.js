/* Script to show hidden section on button click from https://www.w3schools.com/howto/howto_js_toggle_hide_show.asp */
function hiddenA() {
  var x = document.getElementById("hiddenA");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }

  var x = document.getElementById("hiddenA2");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}

